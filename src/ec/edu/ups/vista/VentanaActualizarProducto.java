    private void btnActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActualizarActionPerformed
        // TODO add your handling code here:

        //Cambiar los datos
        Producto productoActualizado = new Producto();
        productoActualizado.setCodigo(Integer.parseInt(txtCodigo.getText()));
        productoActualizado.setNombre(txtNombre.getText());
        productoActualizado.setPrecio(Double.parseDouble(txtPrecio.getText()));
        controladorProducto.update(productoActualizado);

        //Mensaje de Actualizacion
        JOptionPane.showMessageDialog(this, "Producto Modificado");

        //Limpiar el texto Modificado
        txtCodigo.setText("");
        txtNombre.setText("");
        txtPrecio.setText("");

    }//GEN-LAST:event_btnActualizarActionPerformed

