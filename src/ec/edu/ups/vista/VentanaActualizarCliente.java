    //Boton para actulizar los cambios hechos en los datos del Cliente
    private void btnActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActualizarActionPerformed
        // TODO add your handling code here:
        
        //Cambiar los datos
        Cliente clienteActualizado = new Cliente();
        clienteActualizado.setCodigo(Integer.parseInt(txtCodigo.getText()));
        clienteActualizado.setCedula(txtCedula.getText());
        clienteActualizado.setNombre(txtNombre.getText());
        clienteActualizado.setDireccion(txtDireccion.getText());
        clienteActualizado.setTelefono(txtTelefono.getText());
        controladorCliente.update(clienteActualizado);
        
        //Mensaje de Actualizacion
        JOptionPane.showMessageDialog(this, "Cliente Modificado");
                
        //Limpiar el texto Modificado
        txtCodigo.setText("");
        txtCedula.setText("");          
        txtNombre.setText("");
        txtDireccion.setText("");
        txtTelefono.setText("");
    }//GEN-LAST:event_btnActualizarActionPerformed

