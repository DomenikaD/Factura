    //Boton para buscar Codigo del Cliente
    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        // TODO add your handling code here:
        //Se busca los datos por el codgigo y muestra los datos de ese cliente
        int codigo = Integer.parseInt(txtCodigo.getText());
        Cliente buscarCliente = controladorCliente.read(codigo);
        txtNombre.setText(buscarCliente.getNombre());
        txtCedula.setText(buscarCliente.getCedula());
        txtDireccion.setText(buscarCliente.getDireccion());
        txtTelefono.setText(buscarCliente.getTelefono());
    }//GEN-LAST:event_btnBuscarActionPerformed


